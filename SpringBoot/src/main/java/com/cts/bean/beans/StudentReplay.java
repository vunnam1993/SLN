package com.cts.bean.beans;

public class StudentReplay {

	public int ids;
	public String name;
	public String replayAdress;
	public String status;
	
	
	public int getIds() {
		return ids;
	}
	public void setIds(int ids) {
		this.ids = ids;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getReplayAdress() {
		return replayAdress;
	}
	public void setReplayAdress(String replayAdress) {
		this.replayAdress = replayAdress;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
